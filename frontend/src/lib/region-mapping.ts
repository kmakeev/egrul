/**
 * Получить название региона по коду
 */
export function getRegionName(code: string): string {
  return REGION_MAPPING[code]?.name || code;
}

/**
 * Получить код региона по названию
 */
export function getRegionCode(name: string): string | undefined {
  const entry = Object.values(REGION_MAPPING).find(
    (r) => r.name === name || r.nameEn === name
  );
  return entry?.code;
}
